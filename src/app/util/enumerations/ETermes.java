package app.util.enumerations;

/**
 * Cette énumération représente les différents termes utilisés pour décrire la durée de quelque chose, tels que court, moyen et long.
 */
public enum ETermes {
    COURT, // Terme indiquant une durée courte.
    MOYEN, // Terme indiquant une durée moyenne.
    LONG   // Terme indiquant une durée longue.
}